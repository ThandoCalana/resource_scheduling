def normalize_tasks(tasks: list[dict[str, Any]]) -> pd.DataFrame:
    rows = []
    for task in tasks:
        assignees = task.get("assignees", []) or []
        tags = task.get("tags", []) or []

        row = {
            "Name": first_or_none(assignees).get("username") if first_or_none(assignees) else None,
            "Expire Date": extract_custom_field_value(task, 1),
            "date_created": task.get("date_created"),
            "Update Date": task.get("date_updated"),
            "due_date": task.get("due_date"),
            "Certification Name": task.get("name"),
            "CU Status": (task.get("status") or {}).get("status"),
            "Tag": first_or_none(tags).get("name") if first_or_none(tags) else None,
        }
        rows.append(row)

    df = pd.DataFrame(rows)
    return df

