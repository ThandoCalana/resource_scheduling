# -----------------------------
# Base rule: expiry = obtained_date + 2 years
df_temp["expiry_date"] = df_temp["obtained_date"] + pd.DateOffset(years=2)

# -----------------------------
# 2. Define hierarchy ranking
# -----------------------------
hierarchy_rank = {
    "Technology Only": 0,
    "Entry": 1,
    "Core": 2,
    "Associate": 3,
    "Administrator": 4,
    "Specialist": 5,
    "Advanced": 6,
    "Professional": 7,
    "Other": -1,
    "Planning / Research": -1,
    "Unspecified": -1
}

df_temp["hierarchy_rank"] = df_temp["hierarchy"].map(hierarchy_rank).fillna(-1)


df_sorted = df_temp.sort_values(
    by=["employee_name", "technology", "hierarchy_rank", "obtained_date"],
    ascending=[True, True, False, False]
)


highest_cert = (
    df_sorted.groupby(["employee_name", "technology"], as_index=False)
    .first()[["employee_name", "technology", "expiry_date"]]
    .rename(columns={"expiry_date": "group_expiry_date"})
)

# -----------------------------
# 4. Update expiry dates
# -----------------------------

df_temp = df_temp.merge(highest_cert, on=["employee_name", "technology"], how="left")
df_temp["expiry_date"] = df_temp["group_expiry_date"]
df_temp.drop(columns=["group_expiry_date"], inplace=True)

print(df_temp[[
    "employee_name", "certification", "technology",
    "hierarchy", "obtained_date", "expiry_date"
]])

# First, drop existing Expiration Date from final_df to avoid conflicts on update
if 'Expiration Date' in final_df.columns:
    final_df = final_df.drop(columns=['Expiration Date'])

# Select and rename columns from df_temp for merging to avoid conflicts
df_temp_to_merge = df_temp[['Date', 'employee_name', 'certification', 'hierarchy', 'expiry_date']].copy()
df_temp_to_merge.rename(columns={'hierarchy': 'hierarchy_temp', 'expiry_date': 'expiry_date_temp'}, inplace=True)

final_df = final_df.merge(
    df_temp_to_merge,
    left_on=['Date', 'Name', 'Certification Name'],
    right_on=['Date', 'employee_name', 'certification'],
    how='left'
    # suffixes argument removed as explicit renaming is used
)

final_df['hierarchy'] = final_df['hierarchy_temp']
final_df['Expiration Date'] = final_df['expiry_date_temp']

final_df = final_df[final_df['Status'] != 'Planned - No Exam Date']
